export function buildUseVoicePublicApi({
  state = {},
  actions = {},
} = {}) {
  return {
    channelId: state.channelId,
    muted: state.muted,
    deafened: state.deafened,
    speaking: state.speaking,
    peers: state.peers,
    joinError: state.joinError,
    joinChannel: actions.joinChannel,
    leaveChannel: actions.leaveChannel,
    toggleMute: actions.toggleMute,
    toggleDeafen: actions.toggleDeafen,
    setOutputDevice: actions.setOutputDevice,
    setUserVolume: actions.setUserVolume,
    setMicGain: actions.setMicGain,
    voiceProcessingMode: state.voiceProcessingMode,
    setVoiceProcessingMode: actions.setVoiceProcessingMode,
    voiceDiagnostics: state.voiceDiagnostics,
    liveVoiceFallbackReason: state.liveVoiceFallbackReason,
    toggleNoiseSuppression: actions.toggleNoiseSuppression,
    screenSharing: state.screenSharing,
    screenShareStream: state.screenShareStream,
    screenShareDiagnostics: state.screenShareDiagnostics,
    startScreenShare: actions.startScreenShare,
    stopScreenShare: actions.stopScreenShare,
    incomingScreenShares: state.incomingScreenShares,
    showSourcePicker: state.showSourcePicker,
    confirmScreenShare: actions.confirmScreenShare,
    cancelSourcePicker: actions.cancelSourcePicker,
    screenShareError: state.screenShareError,
    clearScreenShareError: actions.clearScreenShareError,
    voiceE2E: state.voiceE2E,
    e2eWarning: state.e2eWarning,
  };
}
