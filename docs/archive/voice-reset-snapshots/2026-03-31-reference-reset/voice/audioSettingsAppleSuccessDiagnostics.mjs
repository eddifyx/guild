export function buildAudioSettingsAppleSuccessDiagnostics({
  testStartedAt,
  activeVoiceMode,
  helperMetadata = {},
  summarizeAudioContextFn = () => ({}),
  ctx,
  voiceNoiseSuppressionBackendApple,
  noiseSuppressionEnabled = true,
  helperStartMs = null,
  monitorPlaybackState = 'starting',
  monitorPlaybackError = null,
  activeOutputId = null,
  monitorProfile = {},
  requestedOutputDeviceId = null,
  usedDefaultOutputFallback = false,
  audioGraphSetupMs = null,
  monitorSetupMs = null,
  totalMs = null,
} = {}) {
  return {
    updatedAt: new Date().toISOString(),
    startedAt: testStartedAt,
    mode: activeVoiceMode,
    requestedConstraints: null,
    usedDefaultDeviceFallback: false,
    sourceTrack: {
      kind: 'audio',
      label: 'Apple voice processing',
      readyState: 'live',
      enabled: true,
      muted: false,
      settings: {
        sampleRate: helperMetadata.sampleRate || 48000,
        channelCount: helperMetadata.channels || 1,
      },
    },
    audioContext: summarizeAudioContextFn(ctx),
    filter: {
      backend: voiceNoiseSuppressionBackendApple,
      requestedBackend: voiceNoiseSuppressionBackendApple,
      suppressionEnabled: noiseSuppressionEnabled,
      loaded: true,
      requiresWarmup: false,
      fallbackReason: null,
      workletCreateMs: helperStartMs,
    },
    playback: {
      state: monitorPlaybackState,
      error: monitorPlaybackError,
      outputDeviceId: activeOutputId || null,
      outputDeviceLabel: monitorProfile.label || null,
      monitorProfile: monitorProfile.id,
      monitorGain: monitorProfile.gain,
      requestedOutputDeviceId: requestedOutputDeviceId || null,
      usedDefaultOutputFallback,
    },
    timingsMs: {
      audioGraphSetup: audioGraphSetupMs,
      helperStart: helperStartMs,
      monitorSetup: monitorSetupMs,
      total: totalMs,
    },
  };
}
