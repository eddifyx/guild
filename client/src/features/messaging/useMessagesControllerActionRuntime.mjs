import { useMemo } from 'react';

import { createMessageControllerActions } from './messageControllerActions.mjs';

export function useMessagesControllerActionRuntime({
  conversation = null,
  user = null,
  userId = null,
  socket = null,
  messages = [],
  loading = false,
  hasMore = true,
  perfTraceId = null,
  refs = {},
  setters = {},
  flows = {},
  clearDeferredRoomSenderKeySync = () => {},
  windowObject = globalThis.window || globalThis,
  consoleWarnFn = () => {},
  consoleErrorFn = () => {},
} = {}) {
  const {
    pendingSentMessagesRef,
    messagesRef,
    prevConvRef,
    retryFailedVisibleRoomMessagesRef,
    deferredRoomSenderKeySyncTimeoutRef,
  } = refs;

  return useMemo(() => createMessageControllerActions({
    socket,
    conversation,
    user,
    userId,
    messages,
    loading,
    hasMore,
    perfTraceId,
    pendingSentMessagesRef,
    messagesRef,
    prevConvRef,
    retryFailedVisibleRoomMessagesRef,
    deferredRoomSenderKeySyncTimeoutRef,
    clearDeferredRoomSenderKeySyncFn: clearDeferredRoomSenderKeySync,
    setLoadingFn: setters.setLoadingFn,
    setMessagesFn: setters.setMessagesFn,
    setHasMoreFn: setters.setHasMoreFn,
    setErrorFn: setters.setErrorFn,
    flows,
    windowObject,
    consoleWarnFn,
    consoleErrorFn,
  }), [
    clearDeferredRoomSenderKeySync,
    conversation,
    deferredRoomSenderKeySyncTimeoutRef,
    flows,
    hasMore,
    loading,
    messages,
    messagesRef,
    pendingSentMessagesRef,
    perfTraceId,
    prevConvRef,
    retryFailedVisibleRoomMessagesRef,
    setters.setErrorFn,
    setters.setHasMoreFn,
    setters.setLoadingFn,
    setters.setMessagesFn,
    socket,
    user,
    userId,
    windowObject,
    consoleWarnFn,
    consoleErrorFn,
  ]);
}
